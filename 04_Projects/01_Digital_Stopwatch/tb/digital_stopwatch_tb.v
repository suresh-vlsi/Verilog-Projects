`timescale 1ns/1ps

//==============================================================
// Testbench : digital_stopwatch_tb
//==============================================================

module digital_stopwatch_tb;

//==============================================================
// Parameters
//==============================================================

parameter CLK_FREQ = 10;

//==============================================================
// Testbench Signals
//==============================================================

reg clk;
reg rst;

reg start;
reg stop;

wire running;
wire [5:0] seconds;
wire [5:0] minutes;

//==============================================================
// DUT
//==============================================================

digital_stopwatch #(
    .CLK_FREQ(CLK_FREQ)
)
dut
(
    .clk(clk),
    .rst(rst),

    .start(start),
    .stop(stop),

    .running(running),

    .seconds(seconds),
    .minutes(minutes)
);

//==============================================================
// Clock Generation
// 10 ns Period
//==============================================================

always #5 clk = ~clk;

//==============================================================
// Monitor
//==============================================================

initial
begin
    $monitor("[%0t] RUN=%b MIN=%02d SEC=%02d START=%b STOP=%b",
              $time,
              running,
              minutes,
              seconds,
              start,
              stop);
end

//==============================================================
// Dump Waveform
//==============================================================

initial
begin
    $dumpfile("digital_stopwatch.vcd");
    $dumpvars(0, digital_stopwatch_tb);
end

//==============================================================
// Stimulus
//==============================================================

initial
begin

    //----------------------------------------------------------
    // Initialize
    //----------------------------------------------------------

    clk   = 0;
    rst   = 1;

    start = 0;
    stop  = 0;

    //----------------------------------------------------------
    // Apply Reset
    //----------------------------------------------------------

    #20;
    rst = 0;

    //----------------------------------------------------------
    // Start Stopwatch
    //----------------------------------------------------------

    #20;
    start = 1;

    #10;
    start = 0;

    //----------------------------------------------------------
    // Run for approximately 15 seconds
    //----------------------------------------------------------

    repeat(150)
        @(posedge clk);

    //----------------------------------------------------------
    // Pause
    //----------------------------------------------------------

    stop = 1;

    @(posedge clk);

    stop = 0;

    //----------------------------------------------------------
    // Hold while paused
    //----------------------------------------------------------

    repeat(50)
        @(posedge clk);

    //----------------------------------------------------------
    // Resume
    //----------------------------------------------------------

    start = 1;

    @(posedge clk);

    start = 0;

    //----------------------------------------------------------
    // Continue running
    //----------------------------------------------------------

    repeat(500)
        @(posedge clk);

    //----------------------------------------------------------
    // Finish
    //----------------------------------------------------------

    $display("----------------------------------------");
    $display("Simulation Completed Successfully");
    $display("----------------------------------------");

    $finish;

end

endmodule